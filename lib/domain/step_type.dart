/// Enum représentant les différents types d'étapes d'une session d'entraînement
enum StepType {
  /// Étape de préparation avant le début des répétitions
  preparation,
  
  /// Étape de travail (effort)
  work,
  
  /// Étape de repos entre les répétitions
  rest,
  
  /// Étape de refroidissement après toutes les répétitions
  cooldown,
}

